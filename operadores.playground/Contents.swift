import UIKit

// Operador de asignación  =
let numeroConstante = 18

// Operadores aritméticos + - * /
var numeroVariable = 2
numeroConstante + numeroVariable
numeroConstante - numeroVariable
numeroConstante * numeroVariable
numeroConstante / numeroVariable

// Operadores compuestos
numeroVariable += 2 // es lo mismo que numeroVariable = numeroVariable + 2 --> y luego le asigna el valor del resultado, que es 4
numeroVariable -= 2 // es lo mismo que numeroVariable = numeroVariable - 2 --> como el numero asignado en la linea anterior fue de de 4, el resultado es lo mismo que 4 - 2 --> y luego le asigna el valor del resultado, que es 2
numeroVariable *= 2 // es lo mismo que numeroVariable = numeroVariable * 2 --> como el numero asignado en la linea anterior fue de de 2, el resultado es lo mismo que 2 * 2 --> y luego le asigna el valor del resultado, que es 4
numeroVariable /= 2 //es lo mismo que numeroVariable = numeroVariable / 2 --> como el numero asignado en la linea anterior fue de de 4, el resultado es lo mismo que 4 / 2 --> y luego le asigna el valor del resultado, que es 2

// Operadores de comparacion
var x = 1
var y = 2
// Igual a ==
x == y
// Diferente a !=
x != y
// Mayor que >
x > y
// Menor que <
x < y
// Mayor o igual que >=
x >= y
// Menor o igual que <=
x <= y
